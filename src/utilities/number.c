#include "ft_printf.h"

int		ft_max(int a, int b)
{
	return (a > b ? a : b);
}

int		ft_get_base(char c)
{
	if (c == 'X' || c == 'x' || c == 'p')
		return (16);
	else if (c == 'o')
		return (8);
	else
		return (10);
}
